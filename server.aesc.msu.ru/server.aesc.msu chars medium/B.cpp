#include <bits/stdc++.h>
using namespace std;

#define len(x) int((x).size())
#define all(x) (x).begin(), (x).end()

int main() {
    char c_1, c_2;
    cin >> c_1 >> c_2;
    if (c_1 != 'A' || c_2 != 'B')
        return cout << "NO", 0;

    cin >> c_1;
    if (c_1 != 'C' && c_1 != 'D')
        return cout << "NO", 0;

    if (c_1 == 'C') {
        c_2 = c_1;
        cin >> c_1;
        if (c_1 != 'H')
            return cout << "NO", 0;
    }

    char kekos;
    cin >> kekos;
    if (kekos != 'G')
        return cout << "NO", 0;

    int cnt = 0;
    while (cin >> c_1, c_1 != '.') {
        if (c_1 != 'G' && c_1 != 'H')
            return cout << "NO", 0;
        if (c_1 == kekos) {
            cin >> c_1;
            if (c_1 == 'H') {
                cin >> c_1;
                if (c_1 == '.')
                    return cout << "YES", 0;
                else
                    return cout << "NO", 0;
            }
            else
                return cout << "NO", 0;
        }
        kekos = c_1, cnt++;
    }

    if ( (kekos == 'H' && cnt > 1) || (c_2 == 'C' && kekos == 'H') )
        return cout << "YES", 0;
    else
        return cout << "NO", 0;
}

